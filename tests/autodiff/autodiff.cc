#ifndef LIBMD__LONG_DOUBLE__
#define LIBMD__LONG_DOUBLE__
#endif

#include "../../libmd.h"

using namespace std;

template<class X> X f1(X x)
{
    return 2.0*exp(-x*x)-2.0;
}

template<class X> X f2(X x)
{
    X a=0.0;
    if(abs(x)<asin(1.0)) return cos(x);
    else return a*x;
}

int main()
{
    ldf h=0.01;
    for(dual x={-10.0,1.0};x<10.0;x+=h) printf("%.12Lf;%.12Lf;%.12Lf;%.12Lf;%.12Lf;%.12Lf;%.12Lf\n",(ldf)x,f1(x).x,f1(x).dx,(f1(x+h/2.0).x-f1(x-h/2.0).x)/h,f2(x).x,f2(x).dx,(f2(x+h/2.0).x-f2(x-h/2.0).x)/h);
    return EXIT_SUCCESS;
}

